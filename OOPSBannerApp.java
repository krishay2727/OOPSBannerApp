/**
 * OOPSBannerApp
 *
 * Prints OOPS in Banner format.
 *
 * @author Arnav
 * @version 2.0
 */


public class OOPSBannerApp {

    public static void main(String[] args) {

        String[] O = {
                " ***** ",
                "*     *",
                "*     *",
                "*     *",
                "*     *",
                "*     *",
                " ***** "
        };

        String[] P = {
                "****** ",
                "*     *",
                "*     *",
                "****** ",
                "*      ",
                "*      ",
                "*      "
        };

        String[] S = {
                " ***** ",
                "*      ",
                "*      ",
                " ***** ",
                "      *",
                "      *",
                " ***** "
        };

        for (int i = 0; i < 7; i++) {
            String line = String.join("   ", O[i], O[i], P[i], S[i]);
            System.out.println(line);
        }
    }
}
